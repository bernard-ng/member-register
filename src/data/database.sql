CREATE TABLE `members`( 
  `id` INT UNSIGNED NOT NULL AUTO_INCREMENT , 
  `matricule` VARCHAR(255) NULL ,
  `numeroCarte` INT NULL , 
  `prenom` VARCHAR (30) NOT NULL , 
  `nom` VARCHAR(30) NOT NULL , 
  `postnom` VARCHAR(30) NOT NULL , 
  `telephone1` VARCHAR(15) NULL , 
  `telephone2` VARCHAR(15) NULL , 
  `sexe` CHAR(1) NOT NULL , 
  `email` VARCHAR(60) NULL ,
  `lieuNaissance` VARCHAR(255) NOT NULL , 
  `dateNaissance` DATE NOT NULL , 
  `lieuBapteme` VARCHAR(255) NULL ,
  `dateBapteme` DATE NULL , 
  `dateAdhesion` DATE NOT NULL , 
  `niveauEtude` VARCHAR(255) NOT NULL , 
  `adresse` VARCHAR(300) NOT NULL , 
  `ville` VARCHAR(50) NOT NULL , 
  `commune` VARCHAR(50) NOT NULL , 
  `quartier` VARCHAR(50) NOT NULL , 
  `departement1` VARCHAR(100) NULL , 
  `departement2` VARCHAR(100) NULL , 
  `fonction1` VARCHAR(100) NULL , 
  `fonction2` VARCHAR(100) NULL , 
  `depuis1` DATE NULL , 
  `depuis2` DATE NULL , 
  `formationEglise` TEXT NULL , 
  `autresSavoir` VARCHAR(255) NULL ,
  `nomConjoint` VARCHAR(90) NULL , 
  `nombreEnfant` INT NULL , 
  `imageUrl` VARCHAR(500) NOT NULL , 
  PRIMARY KEY(`id`)
) ENGINE = InnoDB;


